function meshrider_support_normalize(string $text): string
{
    $t = trim($text);
    if ($t === '') {
        return '';
    }
    $t = mb_strtolower($t, 'UTF-8');
    $map = [
        'أ' => 'ا', 'إ' => 'ا', 'آ' => 'ا', 'ٱ' => 'ا',
        'ى' => 'ي', 'ة' => 'ه',
    ];
    $t = strtr($t, $map);
    $t = preg_replace('/\s+/u', ' ', $t);
    return $t;
}

